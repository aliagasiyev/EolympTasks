package EolympTasks.MixedTasks;

import java.util.Scanner;

public class sumOfTwo {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int a;
        int b;
        int sum;
        int t = scanner.nextInt();
        for (int i = 0; i < t; i++) {
            a = scanner.nextInt();
            b = scanner.nextInt();
            sum = a + b;
            System.out.println(sum);
        }

    }
}
















